package jsoft.objects;

public class FieldObject {
    private int field_id;
    private String field_name;
    private String field_notes;
    private String field_logo;
    private String field_created_date;
    private boolean field_delete;
    private boolean field_enable;
    private String field_last_modified;
    private int field_author_id;
    private int field_actions;
	public int getField_id() {
		return field_id;
	}
	public void setField_id(int field_id) {
		this.field_id = field_id;
	}
	public String getField_name() {
		return field_name;
	}
	public void setField_name(String field_name) {
		this.field_name = field_name;
	}
	public String getField_notes() {
		return field_notes;
	}
	public void setField_notes(String field_notes) {
		this.field_notes = field_notes;
	}
	public String getField_logo() {
		return field_logo;
	}
	public void setField_logo(String field_logo) {
		this.field_logo = field_logo;
	}
	public String getField_created_date() {
		return field_created_date;
	}
	public void setField_created_date(String field_created_date) {
		this.field_created_date = field_created_date;
	}
	public boolean isField_delete() {
		return field_delete;
	}
	public void setField_delete(boolean field_delete) {
		this.field_delete = field_delete;
	}
	public boolean isField_enable() {
		return field_enable;
	}
	public void setField_enable(boolean field_enable) {
		this.field_enable = field_enable;
	}
	public String getField_last_modified() {
		return field_last_modified;
	}
	public void setField_last_modified(String field_last_modified) {
		this.field_last_modified = field_last_modified;
	}
	public int getField_author_id() {
		return field_author_id;
	}
	public void setField_author_id(int field_author_id) {
		this.field_author_id = field_author_id;
	}
	public int getField_actions() {
		return field_actions;
	}
	public void setField_actions(int field_actions) {
		this.field_actions = field_actions;
	}


    // Các phương thức getter và setter cho các thuộc tính
  
}
